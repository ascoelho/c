#include <stdio.h>
int main() {
	

	//Characters in C 

	char number = '3';
	char letter = 'a';
	char bracket = ']';

	printf("Number: %c , Letter: %c, Bracket: %c\n", number, letter, bracket);
	
	//Numbers in C

	int value = 6;
	float floatingNumber = 50.0;

	printf("Value: %d, Floating Number: %.1f\n", value, floatingNumber);

	return 0;
}
